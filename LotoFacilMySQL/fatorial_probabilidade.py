from math import factorial
f25 = factorial(20)
f15 = factorial(15)
f10 = factorial(5)
print(f"Fatorial de 20 = {f25}")
print(f"Fatorial de 15 = {f15}")
print(f"Fatorial de 5 = {f10}")

c = f25 / (f15 * f10)
print(f"Chances de ganhar é de 1 em {c}")

fatorial = factorial(25) / factorial(25 - 15)
print(f"Números de combinações possíveis: {fatorial/1000}")



